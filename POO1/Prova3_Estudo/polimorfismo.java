public class polimorfismo {
    /*
     *  Polimorfismo vem de poli (muitas) morfis (formas) ou seja, muitas formas
     * 
     * É a habilidade de objetos de uma ou mais classes em responder a uma mesma mensagem de formas diferentes
     * 
     * Há duas maneiras de ocorrer: Sobrecarga (Overloading)
     * e: Sobreescrita (Overrding)
     * 
     * 
     * Existe a "Assinatura" de um método, que é composto pelo seu nome e pelos tipos de seus argumentos, independente dos nomes dos argumentos e do valor de retorno
     * 
     * E a sobrecarga é justamente criar um método com mesmo nome porém com tipos de argumentos diferentes ou quantidades de argumentos diferentes
     * (Polimorfismo Estático)
     * 
     * Agora, na sobreescrita, a classe derivada tem uma função com a mesma assinatura porém um funcionamento diferente
     * O método  da classe derivada sobrepõe a função da classe base.
     * (Polimorfismo Dinâmico)
     * 
     * Classe Abstrata (ou classe virtual) é uma classe incompleta onde alguns ou todos os seus métodos não possuem implementação
     * Elas não podem ser instanciadas, servem apenas para reunir características comuns dos descendentes
     * Palavra reservada abstract
     * 
     * Na UML a classe abstrata é escrito em itálico
     * 
     */


}
