L1 = [['c','d'],'a']

L2 = L1[:]

print(L1)  # [['c', 'd'], 'a']
print(L2)  # [['c', 'd'], 'a']

L1[1] = 'z'

print('-------------------')

print(L1)  # [['c', 'd'], 'z']
print(L2)  # [['c', 'd'], 'a']

L2[0][1] = 'y'

print('-------------------')

print(L1)  # [['c', 'y'], 'z']
print(L2)  # [['c', 'y'], 'a']
 
